/**
 * 代码中的类名、方法名、参数名已经指定，请勿修改，直接返回方法规定的值即可
 *
 * 判断岛屿数量
 * @param grid char字符型二维数组
 * @return int整型
 */
func solve(grid [][]byte) int {
	if len(grid) == 0 {
		return 0
	}

	//	遍历，遇到'1'开始广度优先搜索，岛屿+1，表把遍历到的点标记为'2';遇到'0'或'2'则跳过
	count := 0
	for i := 0; i < len(grid); i++ {
		for j := 0; j < len(grid[0]); j++ {
			switch grid[i][j] {
			case '1':
				count++
				BFS(grid, i, j, -1, -1, len(grid[0]), len(grid))
			case '2', '0':
				//	nothing
			}
		}
	}
	return count
}

type Node struct {
	i    int
	j    int
	next *Node
}

func BFS(grid [][]byte, i, j int, edgeLeft, edgeUp, edgeRight, edgeDown int) {
	queue := &Node{i, j, nil}
	task := queue
	queueEnd := queue

	validNode := func(node *Node) bool {
		if node.i > edgeUp && node.i < edgeDown && node.j > edgeLeft && node.j < edgeRight && grid[node.i][node.j] == '1' {
			return true
		}
		return false
	}

	for task != nil {
		grid[task.i][task.j] = '2'
		left := &Node{task.i, task.j - 1, nil}
		up := &Node{task.i - 1, task.j, nil}
		right := &Node{task.i, task.j + 1, nil}
		down := &Node{task.i + 1, task.j, nil}

		moreTasks := [4]*Node{left, up, right, down}
		for k := 0; k < 4; k++ {
			if validNode(moreTasks[k]) {
				queueEnd.next = moreTasks[k]
				queueEnd = queueEnd.next
			}
		}
		task = task.next
	}
}
