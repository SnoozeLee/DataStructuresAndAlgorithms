/**
 * 代码中的类名、方法名、参数名已经指定，请勿修改，直接返回方法规定的值即可
 *
 * 判断岛屿数量
 * @param grid char字符型二维数组
 * @return int整型
 */
func solve(grid [][]byte) int {
	startTime := time.Now()
	fmt.Println(startTime)
	if len(grid) == 0 {
		return 0
	}

	//	遍历，遇到'1'开始广度优先搜索，岛屿+1，表把遍历到的点标记为'0;遇到'0'则跳过
	count := 0
	for i := 0; i < len(grid); i++ {
		for j := 0; j < len(grid[0]); j++ {
			if grid[i][j] == '1' {
				count++
				BFS(grid, i, j, -1, -1, len(grid[0]), len(grid))
			}
		}
	}
	fmt.Printf("time: %d\n", time.Since(startTime))
	return count
}

type Node struct {
	i    int
	j    int
	next *Node
}

func BFS(grid [][]byte, i, j int, edgeLeft, edgeUp, edgeRight, edgeDown int) {
	queue := &Node{i, j, nil}
	grid[i][j] = '0'
	task := queue
	queueEnd := queue

	for task != nil {
		left := &Node{task.i, task.j - 1, nil}
		up := &Node{task.i - 1, task.j, nil}
		right := &Node{task.i, task.j + 1, nil}
		down := &Node{task.i + 1, task.j, nil}

		moreTasks := [4]*Node{left, up, right, down}
		for k := 0; k < 4; k++ {
			i := moreTasks[k].i
			j := moreTasks[k].j

			if i > edgeUp && i < edgeDown && j > edgeLeft && j < edgeRight && grid[i][j] == '1' {
				grid[moreTasks[k].i][moreTasks[k].j] = '0'
				queueEnd.next = moreTasks[k]
				queueEnd = queueEnd.next
			}
		}
		task = task.next
	}
}
